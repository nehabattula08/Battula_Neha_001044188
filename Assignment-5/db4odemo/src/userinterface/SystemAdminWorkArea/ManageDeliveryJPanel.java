    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        if( txtDeliveryName.getText().isEmpty()||txtDeliveryContact.getText().isEmpty()|| txtDeliveryNumberPlate.getText().isEmpty() || txtDeliveryUserName.getText().isEmpty() || txtDeliveryPassword.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Fields cannot be empty");
            return;
        }
        if(system.getUserAccountDirectory().checkIfUsernameIsUnique(txtDeliveryUserName.getText())){
            DeliveryMan addDelivery = new DeliveryMan(txtDeliveryUserName.getText(), txtDeliveryPassword.getText(), txtDeliveryName.getText(), txtDeliveryContact.getText(), txtDeliveryNumberPlate.getText());

            system.getUserAccountDirectory().addUserAccount(addDelivery);
            system.getDeliveryManDirectory().createDeliveryMan(addDelivery);

            displayTable();

            txtDeliveryName.setText("");
            txtDeliveryContact.setText("");
            txtDeliveryNumberPlate.setText("");
            txtDeliveryUserName.setText("");
            txtDeliveryPassword.setText("");

        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:

        int rNum = tblDeliveryContent.getSelectedRow();
        if(rNum<0){
            JOptionPane.showMessageDialog(this, "Please select a row to delete");
            return;
        }

        else {
            DeliveryMan deleteDelivery = system.getDeliveryManDirectory().getDeliveryList().get(rNum);
            
            
            DeliveryManDirectory deliveryDirectory = system.getDeliveryManDirectory();
            deliveryDirectory.removeDeliveryMan(deleteDelivery);
     
            JOptionPane.showMessageDialog(null, "Delivery Man  "  + txtDeliveryUserName.getText() + " has been deleted");
            displayTable();
            txtDeliveryName.setText("");
            txtDeliveryContact.setText("");
            txtDeliveryNumberPlate.setText("");
            txtDeliveryUserName.setText("");
            txtDeliveryPassword.setText("");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int rNum= tblDeliveryContent.getSelectedRow();
        int flag=1;
        if(rNum<0){
            JOptionPane.showMessageDialog(this, "Please select a row to update");
            return;
        }
        if((txtDeliveryName.getText()==null)|| (txtDeliveryName.getText().trim().isEmpty())){
            JOptionPane.showMessageDialog(null,"Please enter a valid Delivery Name");
            flag=0;
        }

        if((txtDeliveryContact.getText()==null)|| (txtDeliveryContact.getText().trim().isEmpty())){
            JOptionPane.showMessageDialog(null,"Please enter a valid Delivery Contact");
            flag=0;
        }

        if((txtDeliveryNumberPlate.getText()==null)|| (txtDeliveryNumberPlate.getText().trim().isEmpty())){
            JOptionPane.showMessageDialog(null,"Please enter a valid Delivery Number Plate");
            flag=0;
        }

        if((txtDeliveryUserName.getText()==null)|| (txtDeliveryUserName.getText().trim().isEmpty())){
            JOptionPane.showMessageDialog(null,"Please enter a valid Delivery User Name");
            flag=0;
        }

        if(txtDeliveryPassword.getText()==null || (txtDeliveryPassword.getText().toString().trim().isEmpty())){
             JOptionPane.showMessageDialog(null,"Please enter a valid Password");
            flag=0;
         
        }

        if(flag==1){
            DeliveryMan deliveryUpdate = system.getDeliveryManDirectory().getDeliveryList().get(rNum);
            deliveryUpdate.setDeliveryName(txtDeliveryName.getText());
        
   
            deliveryUpdate.setPhone(txtDeliveryContact.getText());
       
           deliveryUpdate.setNamePlate(txtDeliveryNumberPlate.getText());
            deliveryUpdate.setUsername(txtDeliveryUserName.getText());
            deliveryUpdate.setPassword(txtDeliveryPassword.getText());
            system.getDeliveryManDirectory().getDeliveryList().set(rNum,deliveryUpdate);
            displayTable();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

