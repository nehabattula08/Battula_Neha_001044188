    private void displayTable() {
            
        DefaultTableModel model = (DefaultTableModel) tblDeliveryContent.getModel();
        model.setRowCount(0);
        for (DeliveryMan d : system.getDeliveryManDirectory().getDeliveryList()){
             Object[] row = new Object[5];
              
                    row[0] = d.getDeliveryName();
                    row[1] = d.getPhone();
                    row[2] = d.getNamePlate();
                    row[3] = d.getUsername();
                    row[4]= d.getPassword();
                    model.addRow(row);
                    
        }
        
        
    }
